#include "3-calc.h"
/**
 * op_add - adds two integers
 * @a: first number
 * @b: second number
 * Return: sum of these integers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - finds substitution of given numbers
 * @a: first number
 * @b: second number
 * Return: difference of these numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - finds multiplication of two numbers
 * @a: first number
 * @b: second number
 * Return: multiplication of these numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - finds division of two numbers
 * @a: first number
 * @b: second number
 * Return: division of these numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - finds mod of two numbers
 * @a: first number
 * @b: second number
 * Return: mod of these numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
